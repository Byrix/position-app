    <p>
        New to Melbourne? Looking for community?
        Or just interested in learning more about our city's queer history?
        PridePoints is the game for you! A GNSS game designed to help you explore
        Melbourne through a new lens while interacting with others in the LGBTQIA+ community.<br><br>
    </p>
    <hr class="border-surface0" />
    <h2 class="font-bold text-xl italic">How to play</h2>
    <p>
        Click on the locator button and start walking towards the icons on the map. Each icon is an important place
        of queer history in Melbourne. When you arrive at the locations, a pop-up will tell you more. <br><br>
        But what's that? Didn't we call this a game?<br><br> Click 'Play' under the information and start a game with other PridePoints
        players who have visited the same locations. Every time you complete a game, you will earn points and make your own
        mark on the map. Slowly build up your own queer history through interactions with others in the community. <br><br>
    </p>
    <hr class="border-surface0" />
    <h2 class="font-bold italic text-xl">Queer quests</h2>
    <p>
        Don't feel like playing with others? That's okay! You can still earn points by visiting locations and
        completing Queer Quests. These include GNSS enabled walking tours, collaborative story telling, contributing
        to digital community artworks and more. Build up a reputation for yourself on the leaderboard or play anonymously.
        It's your choice! We are just here to support you on your journey, whatever that looks like for you. <br><br>
    </p>
    <hr class="border-surface0" />
    <h2 class="font-bold italic text-xl">Content warning</h2>
    <p>
        We believe that Melbourne is for everyone, but that has not always been the case.
        Homosexuality was illegal in our city until 1982 and until then, queer folks were forced to
        live, meet and express themselves away from the public eye. This means that some of our locations are
        places of violence and discrimination. If you find this triggering, use the 'NSFW' button to
        filter out sexually explicit or upsetting content. Notice something we didn't catch? Press report to help us
        keep PridePoints safe for everyone.<br><br>
    </p>
    <hr class="border-surface0" />
    <h2 class="font-bold italic text-xl">Acknowledgements</h2>
    <p>
        This game is a celebration of progress and a testament to those who protested,
        partied and pushed to change Melbourne into the inclusive city that it is today. PridePoints is a GNSS game adaptation of
        <a href="https://www.queerways.au/"> Queer-ways by LUCIANO and Georgia Keats</a>.
        Created in 2021, Queer-ways is a Melbourne-based collaborative community arts
        project that actualises the queer cartography of Australia by placing its history
        onto interactive, digital maps. The information and data used by PridePoints
        has been downloaded from Queer-ways and is a combination of information from the
        <a href="https://queerarchives.org.au/">Australian Queer Archives (AQuA)</a> and community contributions.<br><br>

